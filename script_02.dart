void main() {
  var a = "variable";
  print(a.runtimeType);

  int b = 5;
  print(b.runtimeType);

  double c = 40.45;
  print(c.runtimeType);

  String d = "Sample";
  print(d.runtimeType);

  bool e = true;
  print(e.runtimeType);

  dynamic f = "dynamic val";
  print(f.runtimeType);

  const g = 123;
  print(g.runtimeType);

  final h = 3.14;
  print(h.runtimeType);
}
